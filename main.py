async def handle_answer(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """ูุนุงูุฌุฉ ุงูุฅุฌุงุจุฉ"""
    query = update.callback_query
    await query.answer()
    
    if query.data == "end_quiz":
        if 'timer_task' in context.user_data:
            context.user_data['timer_task'].cancel()
        await end_quiz(update, context)
        return
    
    if query.data == "skip_question":
        if 'timer_task' in context.user_data:
            context.user_data['timer_task'].cancel()
        
        quiz = context.user_data.get('quiz', {})
        if quiz:
            quiz['current'] += 1
            await send_question(update, context)
        return
    
    # ุงุณุชุฎุฑุงุฌ ุจูุงูุงุช ุงูุฅุฌุงุจุฉ
    _, q_id, answer = query.data.split('_')
    q_id = int(q_id)
    
    quiz = context.user_data.get('quiz', {})
    if not quiz:
        return
    
    q_idx = quiz['current']
    if q_idx >= len(quiz['questions']):
        return
    
    q = quiz['questions'][q_idx]
    user_id = update.effective_user.id
    
    # ุฅูุบุงุก ุงููุคูุช
    if 'timer_task' in context.user_data:
        context.user_data['timer_task'].cancel()
    
    # ุชุญุฏูุซ ุงูุฅุญุตุงุฆูุงุช
    question_stats[q_id]['total'] += 1
    question_stats[q_id]['answers'][answer] += 1
    
    is_correct = (answer == q['correct'])
    if is_correct:
        question_stats[q_id]['correct'] += 1
        user_stats[user_id]['correct'] += 1
        user_stats[user_id]['streak'] += 1
        user_stats[user_id]['best_streak'] = max(
            user_stats[user_id]['best_streak'],
            user_stats[user_id]['streak']
        )
    else:
        question_stats[q_id]['wrong'] += 1
        user_stats[user_id]['wrong'] += 1
        user_stats[user_id]['streak'] = 0
    
    user_stats[user_id]['total_answered'] += 1
    
    # ุญุณุงุจ ุชูุฒูุน ุงูุฅุฌุงุจุงุช
    stats = question_stats[q_id]
    total = stats['total']
    
    result_text = (
        f"{create_header('ูุชูุฌุฉ ุงูุณุคุงู')}\n\n"
        f"{Style.THINK} **{q['text']}**\n\n"
        f"{Style.CHART} **ุชูุฒูุน ุงูุฅุฌุงุจุงุช:**\n"
    )
    
    for opt_key, opt_text in q['options'].items():
        count = stats['answers'][opt_key]
        percent = calculate_percentage(count, total)
        mark = Style.CHECK if opt_key == q['correct'] else Style.CROSS
        result_text += f"{mark} {opt_key}: {percent:.1f}% ({count})\n"
    
    result_text += f"\n{Style.PENCIL} **ุฅุฌุงุจุชู:** {answer}\n"
    
    if is_correct:
        result_text += f"{Style.SUCCESS} **โ ุฅุฌุงุจุฉ ุตุญูุญุฉ!**\n"
        result_text += f"{Style.FIRE} **ุงูุณูุณูุฉ:** {user_stats[user_id]['streak']}\n"
    else:
        result_text += f"{Style.DANGER} **โ ุฅุฌุงุจุฉ ุฎุงุทุฆุฉ**\n"
        result_text += f"{Style.BOOK} **ุงูุฅุฌุงุจุฉ ุงูุตุญูุญุฉ:** {q['correct']}\n"
    
    if 'explanation' in q:
        result_text += f"\n{Style.INFO} **ุดุฑุญ:** {q['explanation']}"
    
    # ุงูุงูุชูุงู ููุณุคุงู ุงูุชุงูู
    quiz['current'] += 1
    
    keyboard = [[InlineKeyboardButton(f"{Style.NEXT} ุงูุณุคุงู ุงูุชุงูู", callback_data="next_question")]]
    await query.edit_message_text(
        result_text,
        reply_markup=InlineKeyboardMarkup(keyboard),
        parse_mode="Markdown"
    )

