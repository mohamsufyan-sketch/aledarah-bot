# ==================== دوال مساعدة للأسئلة ====================
def get_all_questions():
    """إرجاع جميع الأسئلة"""
    return QUESTIONS

def get_questions_by_lesson(lesson):
    """إرجاع أسئلة درس معين"""
    return [q for q in QUESTIONS if q.get('lesson') == lesson]

def get_questions_by_difficulty(difficulty):
    """إرجاع أسئلة بمستوى صعوبة معين"""
    return [q for q in QUESTIONS if q.get('difficulty') == difficulty]

def get_question_by_id(q_id):
    """إرجاع سؤال برقم معرف معين"""
    for q in QUESTIONS:
        if q['id'] == q_id:
            return q
    return None

def get_random_questions(count=10):
    """إرجاع أسئلة عشوائية"""
    import random
    return random.sample(QUESTIONS, min(count, len(QUESTIONS)))

def get_questions_count():
    """إرجاع عدد الأسئلة"""
    return len(QUESTIONS)

def get_lessons_list():
    """إرجاع قائمة الدروس مع عدد الأسئلة"""
    lessons = {}
    for q in QUESTIONS:
        lesson = q.get('lesson', 'عام')
        if lesson not in lessons:
            lessons[lesson] = 0
        lessons[lesson] += 1
    return lessons
